# Viết hàm
# Tính chu vi hình chữ nhật
def cv_chunhat(a, b):
    return (a + b) * 2


# Tính diện tích hình chữ nhật
def dt_chunhat(a, b):
    return a * b


# Tính chu vi hình tam giác
def cv_tamgiac(a, b, c):
    return a + b + c


# Tính diện tích hình tam giác
def dt_tamgiac(h, a):
    return (h * a) / 2


# a là độ dài cạnh đáy, h là độ cao
# Tính chu vi hình tròn
def cv_hinhtron(R):
    return (3, 14 * R)


# Tính diện tích hình tròn
def dt_hinhtron(R):
    return (3, 14 * R**2)
